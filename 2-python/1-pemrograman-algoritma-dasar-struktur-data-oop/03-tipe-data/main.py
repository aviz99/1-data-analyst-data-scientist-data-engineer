# Tipe Data

# Integer / Angka Satuan
data_integer = 1

print("data : ", data_integer)
print("tipe data : ", type(data_integer))

# Float / Desimal
data_float = 1.5
print("data : ", data_float)
print("tipe data : ", type(data_float))

# String / Kumpulan Karakter
data_string = "Asep"
print("data : ", data_string)
print("tipe data : ", type(data_string))

# Boolean / Biner true & false
data_bool = True
print("data : ", data_bool)
print("tipe data : ", type(data_bool))

# Tipe Data Khusus

# Bilangan Kompleks
data_complex = complex(3,5)
print("data : ", data_complex)
print("tipe data : ", type(data_complex))

# Tipe Data Dari Bahasa C++
from ctypes import c_double, c_long

data_c_double = c_double(14.96)
print("data : ", data_c_double)
print("tipe data : ", type(data_c_double))

data_c_long = c_long(20)
print("data : ", data_c_long)
print("tipe data : ", type(data_c_long))


